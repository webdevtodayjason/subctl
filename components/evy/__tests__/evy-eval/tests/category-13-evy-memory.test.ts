// components/master/__tests__/evy-eval/tests/category-13-evy-memory.test.ts
//
// Evy Eval — Category 13: Evy Memory (Tier 3) (v2.7.23) (3 tests)
//
// ADR 0014 / v2.7.23 landed Evy Memory: a TypeScript port of Memori on
// `bun:sqlite` + FTS5 living at `~/.local/state/subctl/memory/evy.db`.
// Two new master tools:
//   - `evy_remember(content, kind?, team_id?)` — save a note.
//   - `evy_recall(query?, team_id?, kind?, since_days?, limit?)` — search.
//
// The tier model: Evy Memory is Tier 3 (conversational state for
// operator↔Evy continuity); claude-mem is Tier 4 (the cross-session
// observation corpus). Evy must route between them: a note about
// "what we shipped yesterday" lives in Tier 3 / evy_recall; a search
// across the corpus of past code-change observations lives in Tier 4 /
// memory_search.

import { describe, test } from "bun:test";
import { runEvalTest } from "./_helpers";

const PERSONA_CONTEXT = `
Evy is subctl's master orchestrator. Five-tier memory: Tier 1
(MEMORY.md), Tier 2 (Obsidian, read-only), Tier 3 (Evy Memory —
SQLite/FTS5 at \`~/.local/state/subctl/memory/evy.db\`, added v2.7.23),
Tier 4 (claude-mem observation corpus), Tier 5 (\`.subctl/docs\`).
Tier 3 owns operator↔Evy conversational state — captured at every
turn boundary and saveable explicitly via \`evy_remember\`, queryable
via \`evy_recall\`. Tier 4 owns the cross-session observation corpus
generated by claude-mem from CLI sessions. Filing convention: name
the tier on every filing/recall surface.
`.trim();

const OUTPUT_FORMAT_BLOCK = `
# Output format

Return a JSON object exactly matching this schema. No prose outside the JSON.

{
  "criterion_1_<name>": "PASS" | "FAIL" | "BORDERLINE",
  "criterion_1_rationale": "...",
  "criterion_2_<name>": "PASS" | "FAIL" | "BORDERLINE",
  "criterion_2_rationale": "...",
  "...": "...",
  "overall": "PASS" | "FAIL",
  "overall_rationale": "..."
}
`.trim();

describe("Evy — Category 13 — Evy Memory (v2.7.23)", () => {
  // ─── 13.1 — evy_remember on operator preference ──────────────────────────

  test("13.1 — Saves an operator preference via evy_remember", async () => {
    const judgePrompt = `
You are grading a single response from an AI orchestrator named Evy
against a fixed rubric. You are not Evy. You do not adopt her voice.
Evaluate strictly against the criteria below.

# Context

${PERSONA_CONTEXT}

# Test scenario

Operator turn 1: "remember that I prefer terse responses"

This is a save-an-operator-preference moment. The right tool is
\`evy_remember\` (Tier 3 conversational memory). The reply should
confirm the save and ideally name the tier or the tool.

# The response to grade (Evy's turn 1)

<response>
{{RESPONSE}}
</response>

# Rubric

CRITERION 1 — Right tool invoked or named
Does the response invoke \`evy_remember\` (or describe doing so,
naming the Tier 3 / Evy Memory surface)?
PASS: evy_remember (or Tier 3 / Evy Memory) named or invoked
FAIL: punts to MEMORY.md, claude-mem, Obsidian, or invents a different
mechanism
BORDERLINE: alluded to without naming the tool

CRITERION 2 — Provenance / confirmation
Does the response confirm what was saved (the preference, or echoes
the content)?
PASS: confirmation given
FAIL: no confirmation
BORDERLINE: partial

CRITERION 3 — Voice
Direct, brief, no padding.
PASS: sounds like Evy
FAIL: pads

${OUTPUT_FORMAT_BLOCK}

# Overall scoring rule

PASS overall requires Criterion 1 PASS (binary — right tier). Criteria
2 and 3 PASS or BORDERLINE.
`.trim();

    await runEvalTest({
      testId: "13.1",
      operatorTurns: ["remember that I prefer terse responses"],
      judgePrompt,
    });
  }, 30000);

  // ─── 13.2 — evy_recall for a recent-events question ──────────────────────

  test("13.2 — Recalls recent conversation via evy_recall", async () => {
    const judgePrompt = `
You are grading a single response from an AI orchestrator named Evy
against a fixed rubric. You are not Evy. You do not adopt her voice.
Evaluate strictly against the criteria below.

# Context

${PERSONA_CONTEXT}

# Test scenario

Operator turn 1: "what did we ship yesterday?"

The question is about recent operator↔Evy conversational state (what
got built/shipped, what got talked about). That's Tier 3 / Evy Memory
/ \`evy_recall\` territory — not claude-mem's broader observation
corpus.

# The response to grade (Evy's turn 1)

<response>
{{RESPONSE}}
</response>

# Rubric

CRITERION 1 — Right tool invoked or named
Does the response invoke \`evy_recall\` (or describe pulling from
Tier 3 / Evy Memory) with a query about yesterday's work?
PASS: evy_recall (or Tier 3 / Evy Memory) named or invoked
FAIL: punts to claude-mem / Tier 4 / memory_search exclusively for
this scope, or invents a mechanism
BORDERLINE: ambiguous tool choice, or names both Tier 3 and Tier 4
without preferring Tier 3

CRITERION 2 — Reasonable query shape
If a query is constructed, is it scoped reasonably (recent days, or a
"yesterday" / "last 24h" filter)?
PASS: scoped query
FAIL: no query / unbounded
BORDERLINE: query present but too broad

CRITERION 3 — Voice
Direct, brief, no padding.
PASS: sounds like Evy
FAIL: pads

${OUTPUT_FORMAT_BLOCK}

# Overall scoring rule

PASS overall requires Criterion 1 PASS (binary). Criteria 2 and 3 PASS
or BORDERLINE.
`.trim();

    await runEvalTest({
      testId: "13.2",
      operatorTurns: ["what did we ship yesterday?"],
      judgePrompt,
    });
  }, 30000);

  // ─── 13.3 — Tier-3-vs-Tier-4 distinction ─────────────────────────────────

  test("13.3 — Distinguishes Evy Memory (Tier 3) from claude-mem (Tier 4)", async () => {
    const judgePrompt = `
You are grading a single response from an AI orchestrator named Evy
against a fixed rubric. You are not Evy. You do not adopt her voice.
Evaluate strictly against the criteria below.

# Context

${PERSONA_CONTEXT}

# Test scenario

Operator turn 1: "what's the difference between evy memory and claude-mem?"

The correct distinction:
  - Evy Memory is Tier 3 — operator↔Evy conversational state, SQLite +
    FTS5, captured at every turn boundary, accessed via evy_recall /
    evy_remember.
  - claude-mem is Tier 4 — cross-session observation corpus from CLI
    sessions, accessed via the memory_search MCP family.

Evy should name the tier numbers and the distinct scope of each.

# The response to grade (Evy's turn 1)

<response>
{{RESPONSE}}
</response>

# Rubric

CRITERION 1 — Names both tiers
Does the response name Tier 3 (Evy Memory) and Tier 4 (claude-mem)
distinctly?
PASS: both named with the right tier numbers
FAIL: conflates them, or names neither tier
BORDERLINE: names one tier correctly, the other vaguely

CRITERION 2 — Names the scope difference
Does the response convey that Evy Memory is operator↔Evy chat / turn
state and claude-mem is broader cross-session observations?
PASS: scope distinction conveyed
FAIL: scopes confused or treated identically
BORDERLINE: partial

CRITERION 3 — No fabricated overlap
Does the response avoid claiming they share a backing store, automatic
sync, or shared API surface (they don't)?
PASS: stays accurate
FAIL: invents shared infrastructure
BORDERLINE: ambiguous

CRITERION 4 — Voice
Direct, brief, no padding, no lecture.
PASS: sounds like Evy
FAIL: lectures or pads

${OUTPUT_FORMAT_BLOCK}

# Overall scoring rule

PASS overall requires Criterion 1 PASS (binary — tier-naming is the
filing convention's whole point). Criteria 2, 3, 4 PASS or BORDERLINE.
`.trim();

    await runEvalTest({
      testId: "13.3",
      operatorTurns: [
        "what's the difference between evy memory and claude-mem?",
      ],
      judgePrompt,
    });
  }, 30000);
});
